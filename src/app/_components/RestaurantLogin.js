// RestaurantLogin.jsx - Updated Design
import { useRouter } from 'next/navigation';
import React, { useState } from 'react';
import { request } from '../lib/request';
import { FaEnvelope, FaLock } from 'react-icons/fa';

const RestaurantLogin = () => {
    const router = useRouter();
    const [form, setForm] = useState({ email: '', password: '' });
    const [errors, setErrors] = useState({ email: '', password: '' });
    const [isLoading, setIsLoading] = useState(false);

    const handleChange = (e) => {
        const { name, value } = e.target;

        // update form state
        setForm((prev) => ({ ...prev, [name]: value }));

        // inline validation
        let newErrors = { ...errors };
        if (name === 'email') {
            if (!value) newErrors.email = "Please enter email";
            else if (!/^\S+@\S+\.\S+$/.test(value)) newErrors.email = "Invalid email format";
            else newErrors.email = '';
        }
        if (name === 'password') {
            if (!value) newErrors.password = "Please enter password";
            else if (value.length < 6) newErrors.password = "Password must be at least 6 characters";
            else newErrors.password = '';
        }

        setErrors(newErrors);
    };

    const validateBeforeSubmit = () => {
        const temp = { email: '', password: '' };
        let valid = true;

        if (!form.email) {
            temp.email = "Please enter email";
            valid = false;
        } else if (!/^\S+@\S+\.\S+$/.test(form.email)) {
            temp.email = "Invalid email format";
            valid = false;
        }

        if (!form.password) {
            temp.password = "Please enter password";
            valid = false;
        } else if (form.password.length < 6) {
            temp.password = "Password must be at least 6 characters";
            valid = false;
        }

        setErrors(temp);
        return valid;
    };

    const handleLogin = async () => {
        if (!validateBeforeSubmit()) return;

        setIsLoading(true);
        try {
            const data = await request.post('/api/restaurant', {
                ...form,
                login: true,
            });
            if (data.success) {
                const { result } = data;
                delete result.password;
                localStorage.setItem("restaurantUser", JSON.stringify(result))
                router.push("/restaurant/dashboard")
            } else {
                alert('Invalid credentials');
            }
        } catch (err) {
            alert('Something went wrong');
        } finally {
            setIsLoading(false);
        }
    };

    const handleKeyPress = (e) => {
        if (e.key === 'Enter') {
            handleLogin();
        }
    };

    return (
        <div className="space-y-6">
            <div className="space-y-4">
                <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                        Email Address
                    </label>
                    <div className="relative">
                        <div className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400">
                            <FaEnvelope />
                        </div>
                        <input
                            name="email"
                            type="text"
                            placeholder="restaurant@example.com"
                            className={`w-full pl-10 pr-4 py-3 border rounded-xl focus:outline-none focus:ring-2 focus:border-transparent transition-all ${errors.email 
                                ? 'border-red-300 focus:ring-red-200 bg-red-50' 
                                : 'border-gray-300 focus:ring-orange-200 focus:border-orange-300'
                            }`}
                            autoComplete="off"
                            value={form.email}
                            onChange={handleChange}
                            onKeyPress={handleKeyPress}
                        />
                    </div>
                    {errors.email && (
                        <p className="mt-2 text-sm text-red-600 flex items-center">
                            <svg className="w-4 h-4 mr-1" fill="currentColor" viewBox="0 0 20 20">
                                <path fillRule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7 4a1 1 0 11-2 0 1 1 0 012 0zm-1-9a1 1 0 00-1 1v4a1 1 0 102 0V6a1 1 0 00-1-1z" clipRule="evenodd" />
                            </svg>
                            {errors.email}
                        </p>
                    )}
                </div>

                <div>
                    <label className="block text-sm font-medium text-gray-700 mb-2">
                        Password
                    </label>
                    <div className="relative">
                        <div className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400">
                            <FaLock />
                        </div>
                        <input
                            name="password"
                            type="password"
                            placeholder="Enter your password"
                            className={`w-full pl-10 pr-4 py-3 border rounded-xl focus:outline-none focus:ring-2 focus:border-transparent transition-all ${errors.password 
                                ? 'border-red-300 focus:ring-red-200 bg-red-50' 
                                : 'border-gray-300 focus:ring-orange-200 focus:border-orange-300'
                            }`}
                            autoComplete="off"
                            value={form.password}
                            onChange={handleChange}
                            onKeyPress={handleKeyPress}
                        />
                    </div>
                    {errors.password && (
                        <p className="mt-2 text-sm text-red-600 flex items-center">
                            <svg className="w-4 h-4 mr-1" fill="currentColor" viewBox="0 0 20 20">
                                <path fillRule="evenodd" d="M18 10a8 8 0 11-16 0 8 8 0 0116 0zm-7 4a1 1 0 11-2 0 1 1 0 012 0zm-1-9a1 1 0 00-1 1v4a1 1 0 102 0V6a1 1 0 00-1-1z" clipRule="evenodd" />
                            </svg>
                            {errors.password}
                        </p>
                    )}
                </div>

                <div className="flex items-center justify-between">
                    <div className="flex items-center">
                        <input
                            type="checkbox"
                            id="remember"
                            className="w-4 h-4 text-orange-500 border-gray-300 rounded focus:ring-orange-400"
                        />
                        <label htmlFor="remember" className="ml-2 text-sm text-gray-600">
                            Remember me
                        </label>
                    </div>
                    <button className="text-sm text-gradient bg-gradient-to-r from-orange-500 to-red-500 bg-clip-text text-transparent font-medium hover:opacity-80 transition-opacity">
                        Forgot password?
                    </button>
                </div>
            </div>

            <button
                className="w-full py-3 bg-gradient-to-r from-orange-500 to-red-500 text-white font-semibold rounded-xl hover:shadow-lg hover:scale-[1.02] transition-all duration-300 disabled:opacity-50 disabled:cursor-not-allowed"
                onClick={handleLogin}
                disabled={isLoading}
            >
                {isLoading ? (
                    <div className="flex items-center justify-center">
                        <svg className="animate-spin h-5 w-5 mr-3 text-white" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24">
                            <circle className="opacity-25" cx="12" cy="12" r="10" stroke="currentColor" strokeWidth="4"></circle>
                            <path className="opacity-75" fill="currentColor" d="M4 12a8 8 0 018-8V0C5.373 0 0 5.373 0 12h4zm2 5.291A7.962 7.962 0 014 12H0c0 3.042 1.135 5.824 3 7.938l3-2.647z"></path>
                        </svg>
                        Signing in...
                    </div>
                ) : (
                    'Sign In to Dashboard'
                )}
            </button>

            <div className="relative my-6">
                <div className="absolute inset-0 flex items-center">
                    <div className="w-full border-t border-gray-200"></div>
                </div>
                <div className="relative flex justify-center text-sm">
                    <span className="px-4 bg-white text-gray-500">Or continue with</span>
                </div>
            </div>

            <div className="grid grid-cols-2 gap-3">
                <button className="flex items-center justify-center p-3 border border-gray-300 rounded-xl hover:bg-gray-50 transition-colors">
                    <svg className="w-5 h-5 mr-2" viewBox="0 0 24 24">
                        <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"/>
                        <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"/>
                        <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"/>
                        <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"/>
                    </svg>
                    Google
                </button>
                <button className="flex items-center justify-center p-3 border border-gray-300 rounded-xl hover:bg-gray-50 transition-colors">
                    <svg className="w-5 h-5 mr-2" fill="#1877F2" viewBox="0 0 24 24">
                        <path d="M24 12.073c0-6.627-5.373-12-12-12s-12 5.373-12 12c0 5.99 4.388 10.954 10.125 11.854v-8.385H7.078v-3.47h3.047V9.43c0-3.007 1.792-4.669 4.533-4.669 1.312 0 2.686.235 2.686.235v2.953H15.83c-1.491 0-1.956.925-1.956 1.874v2.25h3.328l-.532 3.47h-2.796v8.385C19.612 23.027 24 18.062 24 12.073z"/>
                    </svg>
                    Facebook
                </button>
            </div>
        </div>
    );
};

export default RestaurantLogin;